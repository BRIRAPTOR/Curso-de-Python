#Tipo de dato int o long
numero = 15
print(numero, type(numero))

#Tipo de dato flotante
numero_float = 12.5
print(numero_float, type(numero_float))

#Tipo de dato numero conplejo
numero_complejo = 5 + 6j
print(numero_complejo, type(numero_complejo))

#Tipo de dato String
nombre = "este es un String"
print(nombre, type(nombre))

#Tipo de dato booleano
verdadero_falso = 3 == 3
print(verdadero_falso, type(verdadero_falso))
