print("Conjuntos creados con llaves {}\n")

print("Ejemplo 1: {5, 1, 3, 2, 4}")
conjunto = {5, 1, 3, 2, 4}
print(conjunto, "\n")

print("Ejemplo 2: {-1, 0, 5, -2, 1, 4}")
conjunto = {-1, 0, 5, -2, 1, 4}
print(conjunto, "\n")

print("Ejemplo 3: {'e', 'o', 'a', 'u', 'i'}")
conjunto = {"e", "o", "a", "u", "i"}
print(conjunto, "\n")

print("Ejemplo 4: {'e', 5, 'o', '0', 'a', 2, -1, 'i'}")
conjunto = {'e', 5, 'o', '0', 'a', 2, -1, 'i'}
print(conjunto, "\n")

print("Ejemplo 5: {5, 1, 1, 3, 5}")
conjunto = {5, 1, 1, 3, 5}
print(conjunto, "\n")

print("Ejemplo 6: {5, (1, 3), 2, 4}")
# conjunto = {5, [1, 3], 2, 4}
conjunto = {5, (1, 3), 2, 4}
print(conjunto, "\n")

print("Ejemplo 7: set([5, 1, 'u', 2, 4, 'e'])")
lista = [5, 1, 'u', 2, 4, 'e']
conjunto = set(lista)
print(conjunto, "\n")

print("Ejemplo 8: set('Hola')")
conjunto = set('Hola')
print(conjunto, "\n")

