# Usando el método remove()
print("---Utilizando el método remove():")
vocales = {"a", "e", "i", "o", "u"}
vocales.remove("i")

print("vocales.remove('i'):", vocales, "\n")

print("---Utilizando el método discard():")
vocales = {"a", "e", "i", "o", "u"}

vocales.discard('a')
print("vocales.discard('a'):", vocales, "\n")
