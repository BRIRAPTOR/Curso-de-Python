print("Accediendo a las posiciones de una lista de elementos")
marcas = ["Apple", "Samsung", "Xiaomi", "Huawei"]
print(f"Lista completa: {marcas}")

print(f"\n¿Cuántos elementos tiene la lista? - len(marcas) = {len(marcas)}")

print(f"marcas[1] : {marcas[1]}")
print(f"marcas[3] : {marcas[3]}")
print(f"marcas[-1] : {marcas[-1]}")
print(f"marcas[-3] : {marcas[-3]}")
print(f"marcas[1:3] : {marcas[1:3]}")
print(f"marcas[:2] : {marcas[:2]}")
print(f"marcas[1:] : {marcas[1:]}")

print(f"marcas[:] : {marcas[:]}")
print(marcas)
