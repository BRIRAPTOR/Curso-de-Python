numeros = [1, 5, 2, 4, 3, 6, 7, 8, 10, 9]
vocales = ['e', 'o', 'i', 'a', 'u']

print(f"Lista números: {numeros}")
numeros.sort()

print(f"\nLista números: {numeros}")
numeros.sort(reverse=True)
print(f"\nLista números: {numeros}")

