tupla_vacia = ()
print(f"Tupla vacia: {tupla_vacia}")
print(f"El tipó de dato de esta tupla es {type(tupla_vacia)}\n")

tupla_homogenea = (3, 2)
print(f"Tupla homogénea: {tupla_homogenea}")
print(f"El tipó de dato de esta tupla es {type(tupla_homogenea)}\n")

tupla_heterogenea = (3, True, "Hola", 2.5)
print(f"Tupla heterogénea: {tupla_heterogenea}")
print(f"El tipó de dato de esta tupla es {type(tupla_heterogenea)}\n")

tupla_heterogenea2 = ([1, 2, 3], {"uno": 1, "dos": 2}, (3,2))
print(f"Tupla heterogénea: {tupla_heterogenea2}")
print(f"El tipó de dato de esta tupla es {type(tupla_heterogenea2)}\n")

tupla_1_elemento = (1,)
print(f"Tupla de un elemento: {tupla_1_elemento}")
print(f"El tipó de dato de esta tupla es {type(tupla_1_elemento)}\n")

tupla_sin_parentesis = 3, True, "Hola", 2.5
print(f"Tupla  sin paréntesis: {tupla_sin_parentesis}")
print(f"El tipó de dato de esta tupla es {type(tupla_sin_parentesis)}\n")
